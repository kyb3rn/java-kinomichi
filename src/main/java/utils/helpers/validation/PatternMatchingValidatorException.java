package utils.helpers.validation;

public class PatternMatchingValidatorException extends ValidatorException {

    // ─── Constructors ─── //

    public PatternMatchingValidatorException() {
        super();
    }

    public PatternMatchingValidatorException(String message) {
        super(message);
    }

    public PatternMatchingValidatorException(Throwable cause) {
        super(cause);
    }

    public PatternMatchingValidatorException(String message, Throwable cause) {
        super(message, cause);
    }

}
